public class TipeDataString {
    public static void main(String[] args) {
        // Tipe data String merupakan
        // tipe data yang berisi kumpulan
        // karakter. Untuk penulisan tipe data
        // string dapat menggunakan keyword String
        // (S nya huruf besar) dan harus di tulis
        // menggunakan tanda petik dua (double quotes)
        String firstName = "Mathius";
        String lastName = "Kormasela";

        // Concatination
        // untuk mengabungkan string
        System.out.println(firstName + " " + lastName);
    }
}
