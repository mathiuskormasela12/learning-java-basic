public class TipeDataBoolean {
    public static void main(String[] args) {
        // Tipe Data Boolean
        // Tipe data yg hanya memiliki 2 nilai, yaitu true atau false
        // Biasanya digunakan untuk operasi kondisional
        boolean menikah = false;
        boolean graduated = true;

        System.out.println(menikah ? "Sudah Menikah" : "Belum Menikah");
        System.out.println(graduated ? "Sudah Lulus" : "Belim Lulus");
    }
}
