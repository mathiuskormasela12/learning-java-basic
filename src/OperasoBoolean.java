public class OperasoBoolean {
    public static void main(String[] args) {
        // && (and)
        // akan menghasilkna true jika setiap input nya true
        System.out.println(10 > 9 && 10 == 10);
        System.out.println(10 == 10 && 10 < 1);

        // || (or)
        // akan true jika salah satu inputnya nya true
        System.out.println(100 > 1000 || 100 == 100);

        // Negasi
        // pembalik
        System.out.println(!false);
    }
}
