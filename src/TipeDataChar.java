public class TipeDataChar {
    public static void main(String[] args) {
        // Tipe data char
        // Untuk mengimpan single karakter
        // Harus di tulis dengan single qutes (petik satu)
        char nama1 = 'M';
        char nama2 = 'A';
        char nama3 = 'T';

        // Mencetak sesuatu kelayar secara single line
        System.out.print(nama1);
        System.out.print(nama2);
        System.out.print(nama3);

        System.out.println();

        // Mencetak sesuatu kelayar secara multi line
        System.out.println(nama1);
        System.out.println(nama2);
        System.out.println(nama3);
    }
}
